function action(mode, type, selection) {
    if (mode <= 0) {
        cm.dispose();
        return;
    } else {
        if (mode == 1) {
            status++;
        } else {
            status--;
        }

        if (status == 0) {
            var text="你好冒险者，欢迎来到#b装备分解驿站，#k你要分解装备吗？"
            text += "#L0#分解装备#l\r\n";
            text += "\r\n\r\n\r\n\r\n";
            cm.sendSimple(text);
			
        } else {
            if (selstatus == -1) {
                selstatus = selection;
            }
            switch (selstatus) {
                case 0:
                    deleteItemBySlot(selection);
                    break;
                case 1:
                    cm.openNpc(cm.getNpc(), 501);
            }
        }
    }
}

